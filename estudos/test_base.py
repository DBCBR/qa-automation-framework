import pytest
from selenium import webdriver

# --------------------------------------------------------------------------------------------------------------------------
# Variáveis globais
# --------------------------------------------------------------------------------------------------------------------------

global_url = "https://www.petrobras.com.br/"
# global_url = "https://www.vale.com/pt"

# --------------------------------------------------------------------------------------------------------------------------
# Configuração do ambiente de teste
# --------------------------------------------------------------------------------------------------------------------------


@pytest.fixture  # Define o driver do Selenium para os testes
def driver():
    driver = webdriver.Chrome()
    yield driver
    driver.quit()

# --------------------------------------------------------------------------------------------------------------------------
# Teste do título da página da Petrobras
# --------------------------------------------------------------------------------------------------------------------------


def test_titulo_petrobras(driver):  # Testa o título da página da Petrobras
    driver.get(global_url)

    titulo_esperado = "O Brasil é a nossa Energia | Petrobras"
    titulo_obtido = driver.title

    assert titulo_obtido == titulo_esperado, (
        f"Título incorreto. Esperado '{titulo_esperado}', obtido '{titulo_obtido}'"
    )
